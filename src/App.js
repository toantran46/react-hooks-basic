import { useEffect, useState } from 'react';
import queryString from 'query-string';
import './App.scss';
import ColorBox from './components/ColorBox';
import Pagination from './components/Pagination';
import PostList from './components/PostList';
import TodoForm from './components/TodoForm';
import TodoList from './components/TodoList';
import SearchBox from './components/SearchBox';
import Clock from './components/Clock';

function App() {
    const [todoList, setTodoList] = useState([
        { id: 1, title: 'I love Easy Frontend! �� ' },
        { id: 2, title: 'We love Easy Frontend! �� ' },
        { id: 3, title: 'They love Easy Frontend! �� ' },
    ]);

    const [postList, setPostList] = useState([]);
    const [pagination, setPagination] = useState({
        _page: 1,
        _limit: 10,
        _totalRows: 1,
    });
    const [filters, setFilters] = useState({
        _limit: 10,
        _page: 1,
        title_like: 'quis',
    });

    try {
        useEffect(() => {
            async function fetchPostList() {
                //param string    _limit=2&page=1
                const paramString = queryString.stringify(filters);
                const requestUrl = `http://js-post-api.herokuapp.com/api/posts?${paramString}`;
                const response = await fetch(requestUrl);
                const responseJSON = await response.json();
                console.log(responseJSON);

                const { data, pagination } = responseJSON;
                setPostList(data);
                setPagination(pagination);
            };
            fetchPostList();
        }, [filters]);
    } catch (error) {
        console.log('Failed to fecth post list', error.message);
    }

    function handlePageChange(newPage) {
        // console.log('new page', newPage);
        setFilters({
            ...filters,
            _page: newPage,
        });

    }

    function handleTodoClick(todo) {
        const newTodoList = [...todoList];
        const index = newTodoList.findIndex(x => x.id === todo.id);
        if (index < 0) return;
        newTodoList.splice(index, 1);
        setTodoList(newTodoList);
    }

    function handleFormSubmit(formValues) {
        const newTodo = {
            id: todoList.length + 1,
            ...formValues,
        }
        const newTodoList = [...todoList];
        newTodoList.push(newTodo);
        setTodoList(newTodoList);
    }

    function handleSearchBoxChange(newsFilters) {
        console.log("New filters", newsFilters);
        setFilters({
            ...filters,
            _page: 1,
            title_like: newsFilters.searchTerm,
        });
    }
    const [showClock, setShowClock] = useState(true);

    return (
        <div className='app' >
            <h1>React Hooks - Todo List - Post List - SearchBox - Clock</h1>

            {showClock && < Clock />}
            <button onClick={() => setShowClock(false)}>Hide</button>
            {/* {<SearchBox onSubmit={handleSearchBoxChange} />}
            {<PostList posts={postList} />}
            {<Pagination
                pagination={pagination}
                onPageChange={handlePageChange}
            />} */}
            {/* {<ColorBox />} */}
            {/* {<TodoForm onSubmit={handleFormSubmit} />} */}
            {/* {<TodoList todos={todoList} onTodoClick={handleTodoClick} />} */}
        </div>
    );
}

export default App;
